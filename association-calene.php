<h3>Descriptif de l&rsquo;association</h3>



<hr class="wp-block-separator"/>



<p><strong>Présentation de l’association&nbsp;:</strong></p>



<p>L&rsquo;association CALENE est une structure créée par des professeurs de lycée de l&rsquo;enseignement industriel pour permettre à des élèves et étudiants de participer à un projet de course de véhicules à consommation énergétique réduite.</p>



<p>En 2018, l’association a relancé ses activités interrompues depuis 2011. Pour cela elle a participé au Chartres Solar CUP en juin 2018. Cette course de véhicule solaire a été une première dans l’expérience de l’association. </p>



<p>Pour participer à cette course, il a fallu modifier la voiture HURBAN CONCEPT ayant concouru au <a href="https://www.shell.fr/energy-and-innovation/shell-eco-marathon.html">Shell Eco Marathon</a> et à <a href="http://www.educeco.net/">l’EDUC’ECO</a>.</p>



<p>Avec l’aide de l’entreprise <a href="http://reden.solar/">REDEN SOLAR</a> installée dans l’Agenais, nous avons réalisé les panneaux solaires permettant d’alimenter le véhicule en énergie.</p>



<p>Le choix des moteurs n’a malheureusement pas permis d’obtenir les
caractéristiques escomptées. De ce fait, après quelque péripéties, l’équipe a
réussi à faire rouler le véhicule et de ce fait à obtenir un classement
honorable pour une première participation.</p>



<p>L’équipe a remporté le prix spécial du jury</p>



<p>En 2019, l’association inscrit la voiture URBAN CALENE au <a href="https://chartres-solarcup.fr/">CHARTRES SOLAR CUP</a>. Cette course se déroulera les 28 et 29 juin à Chartres (27). Un deuxième véhicule est également inscrit.</p>



<p><strong>Objectifs
pédagogiques&nbsp;:</strong></p>



<p>En
Pré bac, les STI2D ITEC travaillent sur la fabrication d’une soufflerie en vue
de la création d’une nouvelle coque. Les STI2D AC travaillent sur le profile et
le nivelage du nouveau circuit du Solar cup.</p>



<p>Les
BTS SN IR et EC ont des projets d’examen portant sur les acquisitions de la
communication des mesures réalisées sur les véhicules.</p>



<p>Les
BTS CPRP et CPI s’occupent du développement et de la fabrication des moteurs
roue et du véhicule.</p>



<p>Les BTS (tertiaire) SAM ont un groupe de projet travaillant sur le dossier de communication, de recherche de partenaires et de financement pour les véhicules.</p>



<p>Une cagnotte sur internet, des recherches de sponsors sont mis en place. Ils ont permis de financer une partie de la participation de cette année.</p>



<p>Les développements du site internet, des réseaux sociaux sont prévus.</p>



